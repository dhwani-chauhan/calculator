package main.java.com.calculator.theme.properties;


public class Theme {

    private String name;
    private String applicationBackground;
    private String textColor;
    private String equalTextcolor;
    private String operatorBackground;
    private String numberBackground;
    private String equalBackground;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getApplicationBackground() {
        return applicationBackground;
    }

    public void setApplicationBackground(String applicationBackground) {
        this.applicationBackground = applicationBackground;
    }

    public String getTextColor() {
        return textColor;
    }

    public void setTextColor(String textColor) {
        this.textColor = textColor;
    }

    public String getEqualTextcolor() {
        return equalTextcolor;
    }

    public void setEqualTextcolor(String equalTextcolor) {
        this.equalTextcolor = equalTextcolor;
    }

    public String getOperatorBackground() {
        return operatorBackground;
    }

    public void setOperatorBackground(String operatorBackground) {
        this.operatorBackground = operatorBackground;
    }

    public String getNumberBackground() {
        return numberBackground;
    }

    public void setNumberBackground(String numberBackground) {
        this.numberBackground = numberBackground;
    }

    public String getEqualBackground() {
        return equalBackground;
    }

    public void setEqualBackground(String equalBackground) {
        this.equalBackground = equalBackground;
    }
}